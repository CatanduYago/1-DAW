public class App {
    public static int factor (int numero){
        if (numero ==0|| numero == 1){
            System.out.println("Su factorial es 1");
        }   else {
            int resultado = 1;
            for (int i = numero;i <= numero;i++){
                int cuentahecha = resultado *i;
                
     
            }
            return cuentahecha;
        }
        
    }
}
